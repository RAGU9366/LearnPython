# ---------- Reserved Keywords ---------- #

# Reserved keywords are pre-defined words...

# ---------- List of Reserved Keywords ---------- #

# False, None, True
# and, or, not, is, in, with
# if, elif, else
# for, while, break, continue, return
# class, def, lambda, del
# assert, async, await
# from, import, as
# try, except, finally
# global, nonlocal, pass, raise, yield

# ---------- Data types ---------- #

# In python, we never mention data type before the variables (Dynamically Typed)...
# Based on the data we provided, python will automatically assign the corresponding data type...

# ---------- List of Data types ---------- #

# int, float, complex
# bool, str, range
# bytes, bytearray
# list, tuple, set, dict
# frozenset, none

